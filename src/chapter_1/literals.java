package chapter_1;

import static chapter_1.Main.run;

public class literals {
    public static void main(String[] args){
        int age = 34;
        char ch = 'A';
        float f = 5.6f; // for initialize float you have to put f or F after the digit
        double d = 5.19;
        byte b = 100;
        long l = 566666666666666666l; // for initialize long you have to put l or L after the digit
        short s = 29999; // short int upto 29999
        boolean bol = true;
        String str = "Farzine";
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(l);
        System.out.println(s);
        System.out.println(bol);
        System.out.println(str);

        run();
    }
}
